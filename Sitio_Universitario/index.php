			<div>
				El Centro Universitario Municipal de San Fernando, comienza entonces sus actividades, el 17 de abril de 2007.
			</div>

			<div>
				2007 – En su primera etapa, se inauguran las instalaciones de planta baja, 2 aulas para 70 alumnos, sanitarios, recepción, y patio interno, todo con fondos municipales.La primera actividad, es  conveniada con la Universidad Nacional de Gral. Sarmiento.  y Universidad Nacional del Arte. El primer año concurren 625 alumnos.
			</div>

			<div>
				2008 – Se construyen dos aulas planta alta, (una es laboratorio informático totalmente equipada), sanitarios, depósito, sala de reuniones, sala de profesores, recepción, administración, y dirección. Se incorpora la Universidad Nacional de Quilmes. Aumenta la matrícula a 900 alumnos.
			</div>

			<div>
				2009 -  Se incorpora la Universidad Pedagógica de la Pcia. de Buenos Aires, Se firma convenio con la Dirección General de Educación de la Provincia, se incorporan los programas FINES Y COA.
			</div> 

			<div>
				Se firma convenio con Centro de Formación profesional N°401. Ya cursan  más de 1000 alumnos.
			</div>

			<div>
				2010 – Se incorpora la Universidad Nacional de La Plata, FAPE (Fundación Argentina para el Progreso de la Enfermería) Centro de Formación Profesional 404. La  Terminalidad secundaria 1.000 alumnos.  La matrícula es de 1.200 alumnos
			</div>

			<div>
				2011 – Se incorpora ICAPA, (Instituto de Capacitación Aeroportuario) Se finaliza la tercera etapa de construcción, un aula informática, la biblioteca, el auditorio, con sus respectivos sanitarios y camarines, y el patio interno. Se inaugura el Centro Universitario N° 3, Astillero Académico, con cuatro aulas, sanitarios, oficinas de administración, sala de reuniones, galpón técnico. Los alumnos son ya 1.300.
			</div>

			<div>
				2012 – Se inaugura el Centro Universitario N° 2, 8 aulas, sanitarios, oficina de administración, recepción. Se realizan las capacitaciones al personal  municipal de distintas áreas y capacitaciones con empresas. Son ya  1.400 los alumnos.
			</div>

			<div>
				2013 – Se incorpora la Red San Andrés, y la Universidad de San Andrés. Se realiza 1er. Congreso de Educación Inicial. Suman 1.600 los alumnos.
			</div>

			<div>
				2014 – Se firma el convenio con la Comunidad Económica Europea para la capacitación de vecinos. Se realiza el 2do Congreso de Educación y Trabajo.  Cursan 1.800 alumnos.
			</div>

			<div>
				2015 – Se incorporan Centros de Form. Profesional. 401 y 404. Se instala a Escuela de Policía local. Se construyen dos aulas nuevas y la of. de empleo, se realiza la reconstrucción de parque y jardines internos. El alumnado llega a 1900.
			</div>

			<div>
				2016 – Se realiza la refacción del auditorio, cabina de luces, sonido.  Se firma convenio con UBA, El alumnado crece hasta los 2.300 alumnos.
			</div>

			<div>
				2017 – Funcionan Universidad Nacional del Arte, Universidad de Quilmes, Icapa, Universidad Nacional de La Plata, FAPE, Centros de FP N° 401 y 404. Se restaura el auditorio del Centro Universitario, alfombras y butacas. Se cambia a nuevo el equipo de sonido y luces. Se realiza la pintura de todo el edificio. Se cambia toda la señalética del mismo.
			</div>

			<div>
				En 2018 y 2019, se siguen incorporando carreras y cursos, ( distintos cursos aeronauticos, ceremonial y protocolo,  y otros,  y la matrícula llega a 3200 alumnos.
